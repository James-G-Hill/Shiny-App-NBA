#' UI Strategy Tab
#' 
#' @param id The namespace identifier.
#' @noRd
#' 
mod_tabitem_strategy_ui <- function(id) {
  
  ns <- shiny::NS(id)
  
  bs4Dash::tabItem(
    tabName = "tab_strategy",
    shiny::fluidRow(
      shiny::column(
        width = 3,
        shiny::selectizeInput(
          inputId = ns("season"),
          label = "Pick a season",
          choices = NULL,
          multiple = TRUE
        ),
        shiny::selectizeInput(
          inputId = ns("analysed"),
          label = "Pick what to analyse",
          choices = c("team_home", "team_away", "player"),
          multiple = FALSE
        ),
        shiny::selectizeInput(
          inputId = ns("choices"),
          label = NULL,
          choices = NULL,
          multiple = TRUE
        )
      ),
      shiny::column(
        width = 9,
        shiny::plotOutput(ns("strategy_plot"))
      )
    )
  )
  
}

#' Server Strategy Tab
#' 
#' @param id The namespace identifier.
#' @param datasets A list of tibbles.
#' @importFrom dplyr .data
#' @noRd
#' 
mod_tabitem_strategy_server <- function(id, datasets) {
  
  stopifnot(
    is.list(datasets),
    is.data.frame(datasets$match),
    is.data.frame(datasets$play)
  )
  
  shiny::moduleServer(
    id,
    function(input, output, session) {
      
      joined_data <-
        dplyr::left_join(
          x = datasets$play,
          y = datasets$match,
          by = "game_id"
        )
      
      shiny::updateSelectizeInput(
        inputId = "season",
        choices = unique(joined_data$season)
      )
      
      shiny::observeEvent(
        shiny::req(input$analysed),
        {
          shiny::updateSelectizeInput(
            inputId = "choices",
            label = stringr::str_glue("Pick {input$analysed}"),
            choices = unique(joined_data[[input$analysed]]),
            server = TRUE
          )
        }
      )
      
      ggplot_res <- 96 # best resolution for ggplot2 plots
      
      output$strategy_plot <-
        shiny::renderPlot(
          res = ggplot_res,
          {
            shiny::req(
              input$analysed,
              input$choices,
              input$season
            )
            
            joined_data |>
              dplyr::filter(
                .data$season %in% input$season,
                .data[[input$analysed]] %in% input$choices
              ) |>
              dplyr::group_by(
                .data[[input$analysed]],
                .data$period,
                .data$minutes_remaining
              ) |>
              dplyr::summarise(
                free_plays_success = sum(.data$shot_made) / dplyr::n()
              ) |>
              dplyr::ungroup() |>
              dplyr::mutate(
                minutes_remaining =
                  as.character(dplyr::desc(.data$minutes_remaining))
              ) |>
              ggplot2::ggplot() +
              ggplot2::geom_point(
                ggplot2::aes(
                  x = .data$minutes_remaining,
                  y = .data$free_plays_success
                )
              ) +
              ggplot2::facet_grid(rows = ggplot2::vars(.data$period)) +
              ggplot2::theme_bw()
          }
        )
      
    }
  )
  
}
